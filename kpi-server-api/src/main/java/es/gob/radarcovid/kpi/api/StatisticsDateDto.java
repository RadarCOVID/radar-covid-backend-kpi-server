package es.gob.radarcovid.kpi.api;

import java.io.Serializable;
import java.util.Date;

import com.fasterxml.jackson.annotation.JsonFormat;

import io.swagger.v3.oas.annotations.media.Schema;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@Builder
@AllArgsConstructor
@NoArgsConstructor
public class StatisticsDateDto implements Serializable {

    @Schema(description = "Date", pattern = "dd/MM/yyyy")
    @JsonFormat(shape = JsonFormat.Shape.STRING, pattern = "dd/MM/yyyy", timezone = "UTC", locale = "es-ES")
    public Date date;
    
    public StatisticsItemDto applicationsDownloads;
    public StatisticsItemDto communicatedContagions;
}
