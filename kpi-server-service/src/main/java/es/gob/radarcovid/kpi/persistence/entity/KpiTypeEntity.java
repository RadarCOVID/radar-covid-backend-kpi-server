package es.gob.radarcovid.kpi.persistence.entity;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@AllArgsConstructor
@NoArgsConstructor
@Builder
@Entity
@Table(name = "KPI_TYPES")
public class KpiTypeEntity implements Serializable {

    private static final String SEQUENCE_NAME = "SQ_NM_ID_KPI_TYPE";

    @Id
    @SequenceGenerator(name = SEQUENCE_NAME, sequenceName = SEQUENCE_NAME, allocationSize = 1)
    @GeneratedValue(strategy = GenerationType.SEQUENCE, generator = SEQUENCE_NAME)
    @Column(name = "NM_ID_KPI_TYPE")
    private Integer id;

    @Column(name = "DE_KPI_NAME", length = 100)
    private String name;

    @Column(name = "IN_ENABLED")
    private boolean enabled;

}
